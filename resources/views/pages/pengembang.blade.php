  <script>
    // Counter Animation
    function animateCounters() {
      const counters = document.querySelectorAll('.counter');
      const observer = new IntersectionObserver((entries) => {
        entries.forEach(entry => {
          if (entry.isIntersecting) {
            const counter = entry.target;
            const target = parseInt(counter.getAttribute('data-target'));
            const increment = target / 50;
            let current = 0;

            const updateCounter = () => {
              if (current < target) {
                current += increment;
                counter.textContent = Math.ceil(current);
                requestAnimationFrame(updateCounter);
              } else {
                counter.textContent = target;
              }
            };

            updateCounter();
            observer.unobserve(counter);
          }
        });
      });

      counters.forEach(counter => observer.observe(counter));
    }

    // Add more floating particles dynamically
    function createFloatingParticles() {
      const container = document.querySelector('.particle-container');
      const colors = ['bg-blue-400', 'bg-purple-400', 'bg-pink-400', 'bg-cyan-400', 'bg-indigo-400'];
      const sizes = ['w-1 h-1', 'w-1.5 h-1.5', 'w-2 h-2'];

      for (let i = 0; i < 8; i++) {
        const particle = document.createElement('div');
        const randomColor = colors[Math.floor(Math.random() * colors.length)];
        const randomSize = sizes[Math.floor(Math.random() * sizes.length)];
        const randomAnimation = `animate-float-${Math.floor(Math.random() * 5) + 1}`;

        particle.className = `particle absolute ${randomSize} ${randomColor} rounded-full opacity-30 ${randomAnimation}`;
        particle.style.top = Math.random() * 100 + '%';
        particle.style.left = Math.random() * 100 + '%';
        particle.style.animationDelay = Math.random() * 5 + 's';

        container.appendChild(particle);
      }
    }

    // Initialize all effects
    document.addEventListener('DOMContentLoaded', function () {
      animateCounters();
      createFloatingParticles();

      // Add staggered animation to developer cards
      const cards = document.querySelectorAll('.developer-card');
      cards.forEach((card, index) => {
        card.style.animationDelay = `${index * 0.15}s`;
      });
    });
  </script>
